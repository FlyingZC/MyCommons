package com.zc.basic.z02oop.topic;

public class T01StackZhen
{
    public static void main(String[] args)
    {
        int x = 100;
        int y = 1000;
        swap(x, y);
        System.out.println(x);
    }

    public static void swap(int a, int b)
    {
        a = 3;
        b = 4;
    }
}
